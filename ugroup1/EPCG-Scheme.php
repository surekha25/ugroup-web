                            <h3 class="title-two">Export Promotion Capital Goods (EPCG)</h3>
                            <p>The objective of the EPCG scheme is to enhance India’s manufacturing capabilities and improve the quality and quantity of products being exported from India. The scheme mainly benefits Manufacturer exporters with or without supporting manufacturer(s), merchant exporters tied to supporting manufacturer(s) and service provider(s); and service providers who want to expand their business outside India..</p>
                            <p>There are various versions of this scheme. The latest version is “Zero Duty EPCG scheme”. The benefit of the scheme is that it enables businesses to import Capital goods without paying any customs duty at the time of import, with a condition that the business adheres to the export obligation condition. Apart from the Customs Duty, even IGST is also exempted under this scheme. The scheme may also be utilized to procure Capital Goods from Indigenous sources. In case of Indigenous Sourcing of Capital Goods the exporter needs to apply for Invalidation, which helps you to get refund of GST paid within 3 to 4 months of the purchase.</p>
                            <p>Manufacturer exporters with or without supporting manufacturer(s), merchant exporters tied to supporting manufacturer(s) and service provider(s); and service providers can benefit from this scheme.</p>
                            <h3 class="title-two">Capital Goods Eligible for EPCG Scheme</h3>
                            <p>The following types of capital goods can be imported into India at zero customs duty under the EPCG scheme:</p>
                            <p>Plant, machinery, equipment or accessories required for manufacture or production, either directly or indirectly, of goods or for rendering services, including those required for replacement, modernisation, technological upgradation or expansion.</p>
                            
