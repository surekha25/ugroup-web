                            <h3 class="title-two">EPCG Annual Reporting of EO Fulfillment</h3>
                            <p>Authorization holders shall submit to the RA concerned by 30th of June every year, a report on fulfillment of export obligation through online. Such a report shall contain details such as shipping bill / GST invoice number, date of export / supply, description of product exported / supplied and FOB / FOR value of Export / Supply for both specific as well as average export obligation. Any delay in filing such an annual report shall be regularized on payment of Rs. 5000/- late fees for each financial year per authorization.</p>
                            <h3 class="title-two">Automatic Reduction or enhancement of Export Obligation</h3>
                            <p>In excess of the duty saved amount indicated on the authorization by not more than 10%, the authorization shall be deemed to have been enhanced by that proportion. Customs shall automatically allow clearance of such goods without endorsement by RA concerned. The Authorization holder shall furnish additional fee to cover excess imports affected, in terms of duty saved amount, to RA concerned, at the time of application for EODC. Export obligation shall be automatically stand enhance proportionately.</p>
                            <p>Excess / reduced duty saved amount may be a result of change in Dollar rates from time of application to time of import or due to enhanced / reduced quantity of imports</p>
                            <h3 class="title-two">Maintenance Annual Average Export Obligation</h3>
                            <p>The export obligation is over and above, the average level of export achieved by you as an authorization holder in the preceding three licensing years for the same and similar products within the overall export obligation period including the extended period (if any). Such average would be the arithmetic mean of export performance in the previous three years for the same and similar products. If your company is a new establishment then your Annual average export obligation would be zero.</p>
                            <p>The excess exports done towards the average export obligation fulfillment of an EPCG authorization during a year can be used to offset any shortfall in the Average EO done in other year(s) of the EO period or the block period as the case may be, provided Average EO imposed is maintained on an overall basis, within the EO period.</p>
